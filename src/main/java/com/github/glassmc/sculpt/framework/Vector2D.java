package com.github.glassmc.sculpt.framework;

public class Vector2D {

    private final double first, second;

    public Vector2D(double first, double second) {
        this.first = first;
        this.second = second;
    }

    public double getFirst() {
        return first;
    }

    public double getSecond() {
        return second;
    }

}
